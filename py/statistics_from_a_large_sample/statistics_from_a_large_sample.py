class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        if len(count) == 0:
            return []

        minK = -1.0
        maxK = -1.0
        mostK = 0
        kCount = 0
        kSum = 0
        for k in range(len(count)):
          if count[k] != 0 and minK == -1:
            minK = k
          if count[k] != 0:
            maxK = k
            kCount += count[k]
            kSum += k * count[k]
            if count[k] >= count[mostK]:
              mostK = k

        for i in range(255):
            count[i + 1] += count[i]
        mean = kSum / kCount
        median1 = bisect(count, (kCount - 1) / 2)
        median2 = bisect(count, kCount / 2)
        median = (median1 + median2) / 2.0              

        return [minK * 1.0, maxK*1.0, mean, median, mostK*1.0]
          

          

